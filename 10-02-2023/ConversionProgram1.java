//Conversion Programs
//Java Convert String to int
class ConversionProgram1{
	public static void main(String[] args) {
		String s="200";  
		//Converting String into int using Integer.parseInt()  
		int i=Integer.parseInt(s);  
		//Printing value of i  
		System.out.println(i); 
	}
}
/*
OUTPUT
200
*/